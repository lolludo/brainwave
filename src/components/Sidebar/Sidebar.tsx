export default function Sidebar() {
    const pathname = usePathname();
    const { user, logout } = useAuth0();

    const handleLogout = () => {
        logout({ logoutParams: { returnTo: window.location.origin } });
    };

    const navItems = [
        { name: 'Overview', href: '/dashboard', icon: Icons.Dashboard },
        { name: 'Timetable', href: '/dashboard/timetable', icon: () => <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><rect x="3" y="4" width="18" height="18" rx="2" ry="2"></rect><line x1="16" y1="2" x2="16" y2="6"></line><line x1="8" y1="2" x2="8" y2="6"></line><line x1="3" y1="10" x2="21" y2="10"></line></svg> },
        { name: 'My Files', href: '/dashboard/files', icon: Icons.Files },
        { name: 'Subjects', href: '/dashboard/subjects', icon: Icons.Subjects },
        { name: 'AI Helper', href: '/dashboard/chat', icon: Icons.Chat },
        { name: 'Settings', href: '/dashboard/settings', icon: Icons.Settings },
    ];

    return (
        <aside className={styles.sidebar}>
            <div className={styles.logo}>
                DTU <span>Helper</span>
            </div>

            <nav className={styles.nav}>
                {navItems.map((item) => {
                    const isActive = pathname === item.href;
                    const Icon = item.icon;
                    return (
                        <Link
                            key={item.href}
                            href={item.href}
                            className={`${styles.navItem} ${isActive ? styles.active : ''}`}
                        >
                            <div className={styles.icon}><Icon /></div>
                            {item.name}
                        </Link>
                    );
                })}
            </nav>

            <div className={styles.userProfile}>
                <div className={styles.avatar}>
                    {user?.picture ? <img src={user.picture} alt="Avatar" style={{ width: '100%', height: '100%', borderRadius: '50%' }} /> : (user?.name?.[0] || 'U')}
                </div>
                <div className={styles.userInfo}>
                    <div className={styles.userName}>{user?.name || 'User'}</div>
                    <div className={styles.userRole}>{user?.email || 'Student'}</div>
                </div>
                <button onClick={handleLogout} className={styles.logoutBtn} title="Logout">
                    <div className={styles.icon}><Icons.LogOut /></div>
                </button>
            </div>
        </aside>
    );
}
